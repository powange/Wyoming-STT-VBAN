"""Constants for VBAN protocol and Wyoming VBAN satellite."""

VBAN_HEADER_SIZE = 28
VBAN_MAGIC = b"VBAN"

# VBAN sub-protocols
VBAN_PROTOCOL_AUDIO = 0x00
VBAN_PROTOCOL_SERIAL = 0x20
VBAN_PROTOCOL_TEXT = 0x40
VBAN_PROTOCOL_SERVICE = 0x60

# VBAN sample rate table (index -> Hz)
VBAN_SAMPLE_RATES = [
    6000, 12000, 24000, 48000, 96000, 192000, 384000,
    8000, 16000, 32000, 64000, 128000, 256000, 512000,
    11025, 22050, 44100, 88200, 176400, 352800, 705600,
]

# VBAN audio codec types
VBAN_CODEC_PCM = 0x00
VBAN_CODEC_VBCA = 0x10
VBAN_CODEC_VBCV = 0x20
VBAN_CODEC_USER = 0xF0

# VBAN PCM data formats (bits per sample)
VBAN_DATATYPE_UINT8 = 0x00
VBAN_DATATYPE_INT16 = 0x01
VBAN_DATATYPE_INT24 = 0x02
VBAN_DATATYPE_INT32 = 0x03
VBAN_DATATYPE_FLOAT32 = 0x04
VBAN_DATATYPE_FLOAT64 = 0x05

# Sample width in bytes for each data type
VBAN_DATATYPE_WIDTH = {
    VBAN_DATATYPE_UINT8: 1,
    VBAN_DATATYPE_INT16: 2,
    VBAN_DATATYPE_INT24: 3,
    VBAN_DATATYPE_INT32: 4,
    VBAN_DATATYPE_FLOAT32: 4,
    VBAN_DATATYPE_FLOAT64: 8,
}

# Wyoming target audio format
WYOMING_RATE = 16000
WYOMING_WIDTH = 2  # 16-bit
WYOMING_CHANNELS = 1

# Default VBAN port
DEFAULT_VBAN_PORT = 6980
